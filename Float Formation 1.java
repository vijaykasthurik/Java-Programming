import java.io.*;
import java.util.*;

public class Solution {

    public static void main(String[] args) {
        Scanner n=new Scanner(System.in);
        float a=n.nextFloat();
        System.out.printf("%.6f\n",a);
        System.out.printf("%.4f\n",a);
        System.out.println(a);
        System.out.println(Math.round(a));
    }
}
